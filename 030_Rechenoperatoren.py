
# Rechenoperatoren

print(7 + 8)   # 15
print(10 - 3)  # 7
print(9 * 3)   # 27
# Division liefert immer einen FLoat zurück
print(9 / 3)  # 3.0

# Floor-Divsion: // (das gibt es nur in Python)
# Liefert immer den abgerundeten Wert zurück
print(11 // 3)  # 3

# Modulo - Restwert-Division: %
print(10 % 3)  # 1
print(17 % 2)  # 1 - Ungerade Zahl
print(18 % 2)  # 0 - Gerade Zahl
print(4 % 4)   # 0 - Wenn die Zahlen gleich sind, immer 0
print(4 % 7)   # 4 - Wenn die erste Zahl kleiner ist, immer die erste Zahl
print(7 % 4)   # 3

print(84 % 7)  # 0 - Da 0 raus kommt, ist 84 durch 7 teilbar

# Potenz-Operator: **
print(2 ** 8)   # 256 - Byte/8-Bit
print(2 ** 16)  # 65536
print(2 ** 24)  # 16777216 - Anzahl möglicher Farben: 16,7 Millionen
print(2 ** 32)  # 4.294.967.296 - Anzahl möglicher IPv4-Adressen







