
# Vergleichsoperatoren
# Auch Relationale Operatoren
# Evaluiert zu einem Boolean
# Werden in Bedingungen benutzt

# >, <, >=, <=, ==, !=
# >= größer gleich
# <= kleiner gleich
# > größer
# < kleiner
# == gleich
# != ungleich
print(7 > 6)    # True
x = 11
y = 12
print(x == y)   # False
print(13 >= 4)  # True
print(9 != 9)   # False
print(2 < 5)    # True

print(9 >= 9)   # True (True, wenn größer ODER gleich)
print(9 > 9)    # False
