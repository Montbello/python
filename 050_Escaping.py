
# Escaping

# Maskieren - Nimmt einem Zeichen seine syntaktische Bedeutung

# Anf√ºhrungszeichen
print('Er sagte: "Hallo"')        # Er sagte: "Hallo"
print("Let's go!")                # Let's go!
print("Er sagte: \"Hallo\"")      # Er sagte: "Hallo"
print('Er sagte: "Let\'s go!"')   # Er sagte: "Let's go!"
print("Er sagte: \"Let's go!\"")  # Er sagte: "Let's go!"
print("Er sagte: 'Let's go!'")    # Er sagte: 'Let's go!'

# Backslash
print("Dies ist der Zeilenumbruch: \n, der benutzt wird, um Zeilen zu umbrechen.")
print("Dies ist der Zeilenumbruch: \\n, der benutzt wird, um Zeilen zu umbrechen.")

# Tabulator
print("Hello\t\tPaul")  # Hello		Paul




