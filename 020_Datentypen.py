
# Datentypen

# Ganzzahl - Integer
x = 11
print(x)        # 11
print(type(x))  # <class 'int'>

# Fließkommazahl - Float
# Der Punkt als Dezimaltrenner
y = 6.789
print(y)        # 6.789
print(2.0)      # 2.0
print(type(y))  # <class 'float'>

# Zeichenkette - String
# Einfache und doppelte Anführungszeichen sind in Python Synonyme von einander.
# Sprich man kann beide verwenden.
# Nur Strings müssen in Anführungsstrichen stehen
str1 = 'Hallo'
str2 = "Hello"
print(str1)  # Hallo
print(str2)  # Hello
print(type(str1))  # <class 'str'>

# Boolean - Wahrheitswert
# Ein Boolean kann nur zwei verschiedenen Zustände haben: True und False
b1 = True
print(b1)  # True
print(type(b1))  # <class 'bool'>
b2 = False
print(b2)  # False



